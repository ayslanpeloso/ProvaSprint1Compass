
public class Usuario {

	private String nome;
	private float salario;
		
	public void atribuirNome(String nome) {
		this.nome = nome;
	}
	
	public String pegarNome() {
		return nome;
	}
	
	public void atribuirSalario(float salario) {
		this.salario = salario;
	}
	
	public float pegarSalario() {
		return salario;
	}
	
	
	}

